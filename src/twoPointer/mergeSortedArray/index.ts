/**
 * Объединяет два отсортированных массива в один отсортированный массив
 * @param nums1 - первый массив, который будет содержать результат
 * @param m - количество элементов в первом массиве
 * @param nums2 - второй массив
 * @param n - количество элементов во втором массиве
 */
export function merge(nums1: number[], m: number, nums2: number[], n: number): void {
    // Указатели на концы массивов
    let i = m - 1;
    let j = n - 1;
    let k = m + n - 1;
    
    // Пока есть элементы в обоих массивах
    while (i >= 0 && j >= 0) {
        // Если элемент в первом массиве больше, помещаем его в конец
        if (nums1[i] > nums2[j]) {
            nums1[k] = nums1[i];
            i--;
        } else {
            // Иначе помещаем элемент из второго массива
            nums1[k] = nums2[j];
            j--;
        }
        k--;
    }
    
    // Если остались элементы во втором массиве, копируем их
    while (j >= 0) {
        nums1[k] = nums2[j];
        j--;
        k--;
    }
} 